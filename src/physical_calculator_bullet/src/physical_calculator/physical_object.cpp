#include "physical_object.hpp"

////////////////////////////////////////////////////////////////////////////////
// Private Data
////////////////////////////////////////////////////////////////////////////////

struct PhysicalObject::PrivateData {
    std::string _name;
};

////////////////////////////////////////////////////////////////////////////////
// Member Methods
////////////////////////////////////////////////////////////////////////////////

PhysicalObject::PhysicalObject(std::string name) {
    _data = new PrivateData;
    _data->_name = name;
}

PhysicalObject::~PhysicalObject(void) {
    delete _data;
}

std::string PhysicalObject::getName(void) {
    return _data->_name;
}
